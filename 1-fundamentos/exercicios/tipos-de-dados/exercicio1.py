# Crie três variáveis com os seguintes valores: um número inteiro, um número decimal e uma string. Use `type()` para imprimir o tipo de cada uma.

num_inteiro = 27
num_decimal = 3.14
texto = 'Hello, world!'

print(type(num_inteiro))  # Deve imprimir <class 'int'>
print(type(num_decimal))  # <class 'float'>
print(type(texto))        # <class 'str'>